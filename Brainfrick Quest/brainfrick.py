text = "+[----->+++<]>.+++++++++.--.------.+++++++++++++++++++.----------.-----.-------------.++++.+++++++++++++++++++.--------.----.++++++++.-------------------.+++++++++++++++++++++++.--.---------------------.+++++++++++.-----.+++++.---.+++++++++++++++++.------------------.++.+++++++++++++++++++++."

translation_table = str.maketrans({'+': ' (o_o) ', '-': ' (╯°□°）╯︵ ┻━┻ ', '.': ' (-_-) ', '[': ' (^o^) ', ']': ' (T_T) ', '<': ' (o.o) ', '>': ' ( ͡° ͜ʖ ͡°) '})
new_text = text.translate(translation_table)

f = open("brainfrickoutput.txt", "w")
f.write(new_text)
f.close()
